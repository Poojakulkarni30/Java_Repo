package day_11;

public class Greetings {
	
	/* 1. No parameter no return
	   2. No parameter Return value
	   3. Take params No return
	   4. Take params Return a value
	 */
	
	// 1. No parameter no return
	
	public void greetings1() {
		
		System.out.println("Hello....");
	}
	
	 //  2. No parameter Return value
	    
	    public String greetings2() {
	    	
	    	return ("hey how you doing ?");
	    }
	    
	//    3. Take params No return
	    
	    public void greetings3(int a, int b) {
	    	System.out.println("Addition of a and b is: "+(a+b));
	    }
	    
	    //  4. Take params Return a value
	    
	    public String greetings4(String name) {
	    	return ("hello...."+name);
	    }
	    
	    
	    
	    
	    
	    
}
