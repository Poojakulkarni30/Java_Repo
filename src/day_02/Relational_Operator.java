package day_02;

public class Relational_Operator {

	public static void main(String[] args) {
		
		int a=10, b=20;
	// Relationals operators ( it always return value as true or false)
		
		System.out.println("return true if a>b :"+(a>b));
		System.out.println("return false if a<b :"+(a<b));
		System.out.println("return true if a>=b :"+(a>=b));
		System.out.println("return false if a<=b :"+(a<=b));
		System.out.println("return true if a==b :"+(a==b));
		System.out.println("return true if a!=b :"+(a!=b));
	}

}
