package day_02;

public class Increment_Decrement_Operator {

	public static void main(String[] args) {

		int a= 10;
		int b=20;
		// value of post increment either increase if it saved into 3rd variable
		// or that used variable printed again
		
		System.out.println("After decrement "+ a);
		System.out.println("After decrement "+ b);
		
		System.out.println("did post increment on a:" +a++);
		System.out.println("after increment value of a :" +a);
		System.out.println("original value of b :" +b);
		System.out.println(" did post increment on b " +b++);
		System.out.println("after increment value of b: "+b);
		
		
	}

}
