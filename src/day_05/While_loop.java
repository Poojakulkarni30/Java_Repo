package day_05;

public class While_loop {

	public static void main(String[] args) {
 
		/* while loop syntax 
		 *    variable;
		 *    while(condition)
		 *    {
		 *    Statements;
		 *    inc/dec;
		 *    }
		 *    print 1-10 no using while loop
		 */
		int i=1;
		while(i<=10) {
			System.out.println("Values of i = " + i);
			i++;
		}
		
	}

}
