package day_05;

public class Jump_Statement {

	public static void main(String[] args) {
		/* Jumping statement 
		
		 1. continue - it will skip that value 
		 2. break = when variable value equal to given value it will stop the prgram
		 
		 */
		
		for(int i=1; i<=100; i++) {
			
			if (i==5) {
				continue;

			}
			
			System.out.println(i);
		}
		
		
		
		
	}

}
